 Admin  Mahasiswa
BANDUNGMU.COM – Bertempat di auditorium utama, kepengurusan Badan Eksekutif Mahasiswa (BEM) Universitas Muhammadiyah Bandung (UMBandung) periode 2021-2022 resmi dilantik pada Kamis (18/11/2021) siang. <br><br>Presiden mahasiswa (presma) atau Ketua BEM periode 2021-2022 Yudi Yudhiana mengatakan banyak program yang akan dilakukan oleh kepengurusannya. Dua di antaranya yakni sayembara melukis desain batik untuk simbol UMBandung dan standardisasi dari legalitas setiap lembaga kemahasiswaan. ”Apa yang menjadi visi dan misi kami, apa yang menjadi harapan sebelumnya, gagasan-gagasan sebelumnya bisa direalisasikan dengan segera,” ucap Yudi. Dalam visi dan misi organisasi, Yudi bersama anggota kepengurusan lainnya ingin membangun kebersaman dengan lembaga kemahasiswaan, himpunan, demi menciptakan kader generasi emas yang intelektual. <br><br>”Kami akan membangun kebersamaan, semangat kolaboratif, dan semangat perjuangan, itulah yang akan kami usahakan supaya lebih baik ke depannya,” lanjutnya. Sementara itu Presma periode 2020-2021 Panca Pebriana Hidayah berpesan kepada kepenegurusan yang baru supaya memiliki rasa menjaga dengan sesama anggota kepengurusan. ”Kami berharap periode yang sekarang untuk IMM ataupun BEM ke depannya memiliki rasa yang independen dalam mengelola lembaga-lembaga kemahasiswaan supaya lembaga kemahasiswaan ini suci dari berbagai kepentingan apa pun,” ujar Panca. <br><br>Selain itu, ia berharap terhadap lembaga mahasiswa tersebut untuk bisa berkontribusi dengan menghasilkan berbagai prestasi yang berarti. ”Kami juga berharap untuk BEM dan IMM tahun ini bisa berkontribusi membawa mahasiswa UMBandung untuk lebih berprestasi di bidang akademik ataupun non-akademik serta bisa berkontribusi dalam gerakan sosial masyarakat serta gerakan sosial politik,” lanjutnya. Kontribusi positif Harapan besar disampaikan Kepala Bagian Kemahasiswaan dan Pengembangan Karier UMBandung Taufik Maulana, S.Pd.I., M.Pd.<br><br> bahwa kepengurusan BEM yang baru bisa konsisten berkontribusi positif untuk kampus UMBandung agar bisa lebih baik dari sebelumnya. ”Tentu yang paling penting adalah harapan kami dari kampus, BEM mampu berbuat positif dan juga banyak merangkul teman-teman mahasiswa, baik itu di UKM, Hima, atau di IMM dari setiap fakultas. Dengan cara seperti itu, kita bisa sama-sama membangun kampus tercinta ini menjadi lebih baik lagi dan jadi kampus yang lebih berkemajuan diiringi prestasi membanggakan,” kata Taufik.<br><br> Taufik berpesan agar teman-teman dari BEM bisa mendukung program kreativitas mahasiswa (PKM) nantinya bisa berpengaruh terhadap peningkatan kualitas kampus.
”Saya berharap semoga BEM kepengurusan yang baru ini bisa membantu program-program kampus dan bisa menyosialisasikan program kami yang ujung-ujungnya ini adalah dari, oleh, dan untuk kita semua di UMBandung,” turur Taufik.
Acara ini berlangsung meriah dengan tetap menerapkan protokol kesehatan. <br><br>Turut hadir dalam acara Rektor UMBandung dan para wakil rektor, Ketua PC IMM Kota Bandung, dan beberapa tamu lainnya.
Selain pelantikan BEM, dalam acara ini juga dilakukan pembukaan Darul Arqam Madya (DAD) Jaya Korkom IMM UMBandung.***(Firman Katon)
    
